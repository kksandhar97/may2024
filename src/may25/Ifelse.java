package may25;

public class Ifelse {
    public static void main(String[] args) {
        int x = 5;

        if (x<10) {
            System.out.println("x is less than 10");
        }
        else if (x==10) {
            System.out.println("x is equal to 10");
        }
        else if (x>3){
            System.out.println("x is greater than 3");
        }
        else {
            System.out.println("something");
        }
    }
}
