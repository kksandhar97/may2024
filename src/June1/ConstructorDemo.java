package June1;

public class ConstructorDemo {
    int day=11;
    int month=12;
    int year=2023;

    public ConstructorDemo(int day, int month, int year) {
        this.day = day;
        this.month = day;
        this.year = day;
    }

        public void PrintDate(){
            System.out.println(day + "/" + month + "/" + year);
        }
    }


